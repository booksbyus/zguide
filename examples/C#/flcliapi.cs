No-one has translated the flcliapi example into C# yet.  Be the first to create
flcliapi in C# and get one free Internet!  If you're the author of the C#
binding, this is a great way to get people to use 0MQ in C#.

To submit a new translation email it to zeromq-dev@lists.zeromq.org.  Please:

* Stick to identical functionality and naming used in examples so that readers
  can easily compare languages.
* You MUST place your name as author in the examples so readers can contact you.
* You MUST state in the email that you license your code under the MIT/X11
  license.

Subscribe to the email list at http://lists.zeromq.org/mailman/listinfo/zeromq-dev.
